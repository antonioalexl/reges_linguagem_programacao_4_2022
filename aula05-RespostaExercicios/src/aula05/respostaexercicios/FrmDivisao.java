    private void calcular() throws ErroPersonalizado {

        try {
            
            int numerador = Integer.parseInt(txtNumerador.getText());
            
            int denominador = Integer.parseInt(txtDenominador.getText());

            int resultado = numerador / denominador;
            
            txtResultado.setText(Integer.toString(resultado));

        } catch (Exception ex) {
            throw new ErroPersonalizado();
        }

    }


