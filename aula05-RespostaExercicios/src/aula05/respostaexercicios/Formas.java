public  class Formas {
    
    public static float quadrado(float lado1, float lado2){
        return lado1 * lado2;
    }
    
    public static float tringulo(float base, float altura){
        return (base * altura)/2;
    }
    
    
    public static float circulo(float raio){
        return (float)Math.PI * raio;
    }
    
    
}
